#Función que se encarga de dibujar los recuadros que indican el camino
def square(x, y):
    "Draw square using path at (x, y)."
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    #Se dibuja un recuadro con las dimensiones dadas
    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

#Función que regresa la posición de cierto punto en el mapa
def offset(point):
    "Return offset of point in tiles."
    
    #Escala las posiciones en x y en y a las medidas de cada cuadro del mapa.
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

#funcion que valida si el punto indicado por la funcion offset es parte del camino o no
def valid(point):
    "Return True if point is valid in tiles."
    index = offset(point)
    
    #Si las coordenadas del punto son un 0 en tiles, regresa false.
    if tiles[index] == 0:
        return False
    
    #Cambia las coordenadas de index para evaluar la posición de la "esquina" contraria, simulando un cuadrado
    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    #En caso de no cumplir las condiciones anteriores, regresa la posición dada inicialmente (true)
    return point.x % 20 == 0 or point.y % 20 == 0

def world():
    "Draw world using path."
    #indica el color del camino y de las barreras
    bgcolor('black')
    path.color('blue')
    
    #Evalua cuadro por cuadro en "tiles"
    for index in range(len(tiles)):
        tile = tiles[index]
        
        #Si el cuadro es 1 en tiles, se dibuja un cuadrado
        if tile == 1:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)
            path.up()
            path.goto(x + 10, y + 10)
            path.dot(2, 'white')
                     
            

def move():
    "Move pacman and all ghosts."
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            options = [
                vector(5, 0),
                vector(-5, 0),
                vector(0, 5),
                vector(0, -5),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)

def change(x, y):
    "Change pacman aim if valid."
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

